package exercicios;

/* Realizar as 4 operações básicas (+ | - | * | /) entre valores 15 e 7 */


public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("15 + 7 = " + (15 + 7));
        System.out.println("15 - 7 = " + (15 - 7));
        System.out.println("15 * 7 = " + (15 * 7));
        System.out.println("15 / 7 = " + (15 / 7));
       

    }
    
}
