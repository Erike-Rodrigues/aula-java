package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {

       int a = 5;
       int  b = 10;

        //pós-incremento
        System.out.println(a++); // usa a variável e depois soma 1
        System.out.println(a);

        // pré-incremento
        System.out.println(++b); // primeiro soma 1 na variável e depois usa a variável
        System.out.println(b);


        
    }
    
}
