package exemplos;

public class Exemplo03 {
    public static void main(String[] args) {
        int n1, n2;

        n1 = 5;
        n2 = 7;

        // Operadores relacionados sempre geram saida booleana "true or false"
        System.out.println(n1 > n2);
        System.out.println(n1 >= n2);
        System.out.println(n1 == n2);
        System.out.println(n1 != n2);
    }
    
}
